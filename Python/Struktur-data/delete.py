angka = [1, 2, 3, 4, 5] # inisialisasi array
print(angka) # cetak array
# [1, 2, 3, 4, 5]
angka.remove(3) # menghapus elemen 3 dari array
print(angka) # cetak array
# [1, 2, 4, 5]
angka.pop(2) # menghapus elemen pada indeks 2 dari array
print(angka) # cetak array
# [1, 2, 4]
angka.clear() # menghapus semua elemen dari array
print(angka) # cetak array
