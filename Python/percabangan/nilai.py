#menentukan kelulusan siswa berdasarkan nilai

nilai = int(input("Masukan Nilai: ")) #memina input dari user

if nilai >= 70:
    print("Selamat anda lulus! Nilai anda melebihi standar") #jika nilai lebih dari sama dengan 70
    
else:
    print("Mohon maaf anda tidak lulus! Nilai anda dibawah standar") #jika nilai kurang dari 70