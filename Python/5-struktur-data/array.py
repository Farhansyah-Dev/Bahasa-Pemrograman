arr = [10, 20, 30, 40, 50] #inilialisasi array

#Menampilkan isi array menggunakan for loop
for i in range(len(arr)):
    print(f"Elemen ke-{i} = {arr[i]}") #menampilkan elemen array ke-i

#Menampilkan isi array menggunakan while loop
i = 0
while i < len(arr):
    print(f"Elemen ke-{i} = {arr[i]}") #menampilkan elemen array ke-i
    i += 1 #increment i
